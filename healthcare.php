                width: `${initialProgress}%`
            });

            $(".nextButton").click(function () {
                const currentFieldset = $(this).closest(".form-step");
                if (validateFieldset(currentFieldset)) {
                    const nextFieldset = currentFieldset.next(".form-step");
                    if (nextFieldset.length) {
                        currentFieldset.hide();
                        nextFieldset.show();
                        updateProgressBar(nextFieldset);
                    } else {
                        window.location.href = "new_page.html";
                    }
                }
            });

            function updateProgressBar(currentFieldset) {
                const progress = parseInt(currentFieldset.find(".progress-bar").attr("aria-valuenow"));
                progressBar.css({
                    width: `${progress}%`
                });
            }

            function validateFieldset(fieldset) {
                const formControls = fieldset.find(".form-control");
                const formSelects = fieldset.find(".form-select");
                let isValid = true;

                formControls.each(function () {
                    if ($(this).hasClass("healthNumber")) {
                        const phoneNumber = $(this).val();
                        if (!isValidPhoneNumber(phoneNumber)) {
                            isValid = false;
                            return false; // Exit the loop
                        }
                    } else if ($(this).hasClass("integerNumber") && !isValidIntegerNumber($(this).val())) {
                        isValid = false;
                        return false; // Exit the loop
                    } else if ($(this).val().trim() === "") {
                        isValid = false;
                        return false; // Exit the loop
                    }
                });

                formSelects.each(function () {
                    if ($(this).val() === "Select From Dropdown") {
                        isValid = false;
                        return false; // Exit the loop
                    }
                });

                if (!isValid) {
                    fieldReq.show();
                } else {
                    fieldReq.hide();
                }

                const button = fieldset.find(".nextButton");
                button.prop("disabled", !isValid); // Enable/disable the button based on validation

                return isValid;
            }

            function isValidPhoneNumber(phoneNumber) {
                const pattern = /^\(\d{3}\) \d{3}-\d{4}$/;
                return pattern.test(phoneNumber);
            }

            function isValidIntegerNumber(number) {
                return /^\d+$/.test(number);
            }

            // Initialize input mask for phone number input
            Inputmask({
                "mask": "(999) 999-9999"
            }).mask($(".healthNumber"));

            $(".finishButton").click(function () {
                const finalFieldset = fieldsets.last();
                if (validateFieldset(finalFieldset)) {
                    $.ajax({
                        url: "submit_form.php",
                        method: "POST",
                        data: $("#healthcareFrom").serialize(),
                        success: function () {
                            window.location.href = "new_page.html";
                        },
                        error: function () {
                            console.error("Form submission error");
                        }
                    });
                }
            });
        });
    </script>

